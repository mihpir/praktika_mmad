#вариант №5
import re

def rex(mas, reg):
    for i in mas:
        result = re.search(reg, i)
        if result != None: print(i, ' yes')
        else: print(i, ' no')
    print()

# 2 задание
mas = ['a@b.c', 'a-b@c.d.e', 'a-b_c.d@e_f-g.h', 'a+@b.c', 'a_b.c', 'a_b@.c-d']
# в начале строки должны встречаться строчные и прописные буквы, цифры, нижние подчёркивания, дефисы и точки 1 и более раз
# затем должен быть символ @
# зетем должны встречаться строчные и прописные буквы, цифры, нижние подчёркивания, дефисы и точки 1 и более раз
# затем должна быть точка
# конец строки должен оканчиваться последовательнотью букв в любом регистре
reg = r'^[\w\-.]+@[\w\-.]+\.[A-Za-z]+$'
rex(mas, reg)

