mas = ['test.png', 'test.jpeg', 'test.jpg', 'test.gif', 'test.php', 'test.exe', '~!@$%.png', '<?php test.png ?>']
# в начале строки должны встречаться строчные или прописные буквы 1 и более раз
# затем должна быть точка
# конец строки должен оканчиваться одним из четырёх наборов символов (png, jpg, jpeg, gif)
reg = r'^[A-Za-z]+\.(png|jpg|jpeg|gif)$'
rex(mas, reg)
